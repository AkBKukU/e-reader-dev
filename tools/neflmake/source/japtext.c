u16 ascii_to_sjis( u8 ascii)
{
	u16 sjis;
	//
	sjis = TABLE_ASCII_TO_SJIS[ascii];
	if (sjis == 0x0000) sjis = TABLE_ASCII_TO_SJIS['.'];
	return ((sjis >> 8) & 0xFF) | ((sjis & 0xFF) << 8);
}